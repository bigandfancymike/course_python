# Напишіть функцію котра приймає 1 аргумент (int)(нехай це буде х) та повертає lambda функцію котра приймає один
# аргумент (нехай буде y) котрий буде піднесений до ступеня аргументу з функції (def), тобто змінна y буде піднесена
